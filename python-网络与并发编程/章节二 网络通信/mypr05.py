# TCP的三次握手和四次挥手_数据包的概念
# 三次握手：
    # 第一次握手：客户端发送连接请求报文段，将SYN位置为1，SeqNum为x
    # 第二次握手：服务器端收到连接请求报文段后，如果同意连接，则发送一个应答，将SYN和ACK位置为1，AckNum为x+1，SeqNum为y
    # 第三次握手：客户端收到连接确认报文段后，还要向服务器端发出确认，将ACK位置为1，AckNum为y+1，SeqNum为x+1

# 四次挥手：
    # 第一次挥手：客户端发送连接释放报文段，FIN位置为1，SeqNum为u
    # 第二次挥手：服务器端收到连接释放报文段后，发出确认，将ACK位置为1，AckNum为u+1，SeqNum为v
    # 第三次挥手：服务器端发送连接释放报文段，FIN位置为1，SeqNum为w
    # 第四次挥手：客户端收到连接释放报文段后，发出确认，将ACK位置为1，AckNum为w+1，SeqNum为u+1

#数据包
    # 数据包是网络传输的基本单位，是网络传输数据的载体
    # 数据包由首部和数据两部分组成
    # 首部包含了数据包的控制信息，如源地址、目的地址、数据包序号等
    # 数据包的大小是有限制的，超过最大大小的数据包需要分片传输
